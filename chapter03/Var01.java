public class Var01 {
	//编写一个main方法
	public static void main(String[] args) {
		//声明变量
		int age = 20;//类型int整型		
		double score = 88.6;//double 表示小数		
		char gender = '男';//char 表示字符
		String name = "jack";//String 表示字符串
		//输出个人信息
		System.out.println("个人信息如下");
		System.out.println("年龄 " + age);
		System.out.println("成绩" + score);
		System.out.println("性别" + gender);
		System.out.println("姓名" + name);

		//变量在同一个作用域内不能重名
		//变量=变量名+值+数据类型 三要素缺一不可


    }
} 	